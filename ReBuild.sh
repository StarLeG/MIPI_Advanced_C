#!/bin/bash

# Полная пересборка проекта
echo "=== Начало пересборки ==="

# Очищаем предыдущую сборку
echo "Очистка build директории..."
rm -rf build

# Создаем новую
echo "Создание новой сборки..."
mkdir -p build
cd build

# Конфигурируем
echo "Конфигурация CMake..."
cmake ..

# Собираем
echo "Сборка проекта..."
make

echo "=== Пересборка завершена ==="